function recreatead_custom1()
{
	recreatead.onreadystatechange(recreatedad_custom1)
	recreatead.send('ajax.php?do=createad&adcode=custom1_adcode')
	function recreatedad_custom1()
	{
		if (recreatead.handler.readyState == 4 && recreatead.handler.status == 200)
		{
			fetch_object('custom1_adcode').innerHTML = recreatead.handler.responseText
		}
	}
}
